export class Calculadora{
    constructor(n1,n2){
        this.n1 = n1;
        this.n2 = n2;
    }

    sumar(n1,n2){
        return n1 + n2;
    }
    restar(n1,n2){
        return n1 - n2;
    }
    dividir(n1,n2){
        return n1 / n2;
    }
    multiplicar(n1,n2){
        return n1 * n2;
    }
    
}